<script>
	function addToCart(){
		debugger;
		var mID = $('#mID').val();
		var mName = $('#mName').html();
		var mPrice = $('#mPrice').html();
		var mAmount = $('#mQty').val();
		var image = $('#img').val();
		var params = { id: mID,name:mName,price:mPrice,amount:mAmount,img:image };
		$.ajax({
			url: '<?php echo base_url()?>index.php/home/addToCart',
			type : "POST",
			dataType:'json',
			data:params,
			success: function(data){
				if(parseInt(data.status) > 0){
					alert(data.message);
				}
			},
			error : function(data){
			alert(data.responseText);
			}
		});
	}
</script>		